    async function uploadAndAnalyze(file) {
        const allowed = [".pdf",".docx",".txt"];
        const ext = file.name.toLowerCase().slice(file.name.lastIndexOf("."));
        if (!allowed.includes(ext)) { showError("Invalid file type. Upload PDF, DOCX, or TXT only."); return; }
        if (file.size > 20971520) { showError("File too large. Max 20MB."); return; }
        // Show loading
        uploadArea.style.display = 'none';
        document.getElementById('analysis-results').style.display = 'none';
        document.getElementById('analysis-loading').style.display = 'block';

        const formData = new FormData();
        formData.append('file', file);
        formData.append('session_id', SESSION_ID);

        const abortAnalyze = new AbortController();
        setTimeout(() => abortAnalyze.abort(), 30000);
        const ctrl1 = new AbortController(); setTimeout(() => ctrl1.abort(), 30000);
        try {
            const res = await fetch(`${API_BASE}/analyze`, {
                method: 'POST',
                signal: abortAnalyze.signal,
                body: formData
            });
            const data = await res.json();

            document.getElementById('analysis-loading').style.display = 'none';

            if (data.success) {
                currentContractLoaded = true;
                currentAnalysis = data.analysis;
                renderAnalysis(data);
                document.getElementById('analysis-results').style.display = 'block';
                updateChatContractInfo(data);
            } else {
                showError(data.detail || 'Analysis failed. Please try again.');
                uploadArea.style.display = 'block';
            }
        } catch (err) {
            document.getElementById('analysis-loading').style.display = 'none';
            showError('Network error. Please check your connection and try again.');
            uploadArea.style.display = 'block';
        }
    }

